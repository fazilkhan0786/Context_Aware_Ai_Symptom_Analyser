import jsPDF from 'jspdf';
import { SymptomAnalysisResult } from '../types/SymptomAnalysisResult';

interface ReportData {
  analysis: SymptomAnalysisResult;
  patientInfo?: {
    age?: string;
    gender?: string;
    allergies?: string;
    chronicConditions?: string;
  };
  symptomDescription: string;
  language: string;
}

